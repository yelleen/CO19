def randTimeSet():
    randInHour = random.randrange(10, 20)
    randInMin = random.randrange(0, 60)
    randStay = random.randrange(10, 30)
    randOutHour = randInHour
    randOutMin = randInMin + randStay
    if randOutMin >= 60:
        randOutMin = randOutMin - 60
        randOutHour = randOutHour + 1
    result = str(randInHour) + ':' + str(randInMin) + ' ' + str(randOutHour) + ':' + str(randOutMin)
    return result

def randRoomSet():
    randRoom = random.randrange(1, 5)
    return str(randRoom)

